// Check for existing projects on startup
async function checkForExistingProjects() {
    const result = await window.electronAPI.listProjects();
    
    if (!result.success) {
        showToast('Failed to check for projects: ' + result.error, 'error');
        return;
    }
    
    if (result.projects.length === 0) {
        // No projects exist, show modal to create one
        showProjectModal();
    } else {
        // Projects exist, load the most recent one or show selection
        showProjectModal();
    }
}

// Show project selection modal
async function showProjectModal() {
    // Display current storage location
    const storageCheck = await window.electronAPI.checkStorageConfigured();
    const storageDisplay = document.getElementById('storage-location-display');
    if (storageCheck.isConfigured && storageCheck.path) {
        storageDisplay.textContent = `${storageCheck.path}/Document Manager/Projects`;
    } else {
        storageDisplay.textContent = 'Not configured';
    }
    
    const result = await window.electronAPI.listProjects();
    
    if (!result.success) {
        showToast('Failed to load projects: ' + result.error, 'error');
        return;
    }
    
    const projectsList = document.getElementById('existing-projects-list');
    
    if (result.projects.length === 0) {
        projectsList.innerHTML = '<p class="empty-state">No existing projects found.</p>';
    } else {
        projectsList.innerHTML = '';
        result.projects.forEach(projectName => {
            const item = document.createElement('div');
            item.className = 'project-list-item';
            item.innerHTML = `
                <span class="project-list-item-name">${projectName}</span>
                <span>→</span>
            `;
            item.addEventListener('click', () => openProject(projectName));
            projectsList.appendChild(item);
        });
    }
    
    document.getElementById('project-modal').style.display = 'flex';
}

// Change storage location
async function changeStorageLocation() {
    if (!confirm('Warning: Changing the storage location will not move your existing projects. You will need to manually move the "Document Manager" folder to the new location.\n\nDo you want to continue?')) {
        return;
    }
    
    const result = await window.electronAPI.setStorageLocation();
    
    if (result.success) {
        showToast(`Storage location updated to: ${result.path}`, 'success');
        // Refresh the project modal
        closeProjectModal();
        await showProjectModal();
    } else if (!result.cancelled) {
        showToast('Failed to change storage location', 'error');
    }
}

// Close project modal
function closeProjectModal() {
    document.getElementById('project-modal').style.display = 'none';
    document.getElementById('new-project-name').value = '';
}

// Create new project
async function createNewProject() {
    const projectName = document.getElementById('new-project-name').value.trim();
    
    if (!projectName) {
        showToast('Please enter a project name', 'error');
        return;
    }
    
    const result = await window.electronAPI.createProject(projectName);
    
    if (!result.success) {
        showToast('Failed to create project: ' + result.error, 'error');
        return;
    }
    
    showToast('Project created successfully!', 'success');
    await openProject(projectName);
    closeProjectModal();
}

// Open existing project
async function openProject(projectName) {
    const result = await window.electronAPI.loadProject(projectName);
    
    if (!result.success) {
        showToast('Failed to load project: ' + result.error, 'error');
        return;
    }
    
    AppState.currentProjectName = projectName;
    AppState.data = result.data;
    AppState.currentDocument = null;
    
    updateProjectName();
    renderDocumentsList();
    renderDocumentEditor();
    closeProjectModal();
    
    showToast('Project loaded: ' + projectName, 'success');
}

// Update project name display
function updateProjectName() {
    const projectNameEl = document.getElementById('current-project-name');
    if (AppState.currentProjectName) {
        projectNameEl.textContent = AppState.currentProjectName;
    } else {
        projectNameEl.textContent = 'No Project';
    }
}

// Save current project
async function saveProjectData() {
    if (!AppState.currentProjectName) {
        showToast('No project loaded', 'error');
        return false;
    }
    
    const result = await window.electronAPI.saveProject(AppState.currentProjectName, AppState.data);
    
    if (!result.success) {
        showToast('Failed to save project: ' + result.error, 'error');
        return false;
    }
    
    return true;
}

// Import Document Bundle
async function importExternalJSON() {
    if (!AppState.currentProjectName) {
        showToast('Please open or create a project first', 'error');
        return;
    }
    
    // Select the bundle folder
    const result = await window.electronAPI.selectDirectory({
        title: 'Select Document Bundle Folder',
        message: 'Choose a folder containing data.json and images',
        buttonLabel: 'Import'
    });
    
    if (!result.success) return;
    
    const bundlePath = result.path;
    
    // Validate bundle structure
    const validation = await validateDocumentBundle(bundlePath);
    
    if (!validation.valid) {
        showToast('Invalid bundle: ' + validation.error, 'error');
        return;
    }
    
    // Load and process the document
    const jsonPath = bundlePath + '/data.json';
    const loadResult = await window.electronAPI.loadJSON(jsonPath);
    
    if (!loadResult.success) {
        showToast('Failed to load data.json: ' + loadResult.error, 'error');
        return;
    }
    
    const bundleData = loadResult.data;
    
    // Handle two formats:
    // 1. Full data.json with documents array: { documents: [...] }
    // 2. Single document object: { id: "...", title: "...", ... }
    let documentToImport;
    
    if (bundleData.documents && Array.isArray(bundleData.documents)) {
        // Format 1: Full structure with documents array
        if (bundleData.documents.length === 0) {
            showToast('No documents found in bundle', 'error');
            return;
        }
        documentToImport = bundleData.documents[0];
    } else if (bundleData.id && bundleData.title && bundleData.sections) {
        // Format 2: Direct document object
        documentToImport = bundleData;
    } else {
        showToast('Invalid bundle format: Expected document object or documents array', 'error');
        return;
    }
    
    const originalId = documentToImport.id;
    
    // Verify document ID matches image folder
    if (validation.imageFolder !== originalId) {
        const shouldContinue = confirm(
            `Warning: Document ID "${originalId}" doesn't match image folder "${validation.imageFolder}".\n\n` +
            `The document ID will be corrected to match the image folder.\n\nContinue?`
        );
        
        if (!shouldContinue) return;
        
        // Correct the document ID to match the image folder
        documentToImport.id = validation.imageFolder;
    }
    
    // Check for duplicates
    const duplicate = AppState.data.documents.find(doc => 
        doc.id === documentToImport.id || doc.title === documentToImport.title
    );
    
    if (duplicate) {
        const action = await promptDuplicateAction(documentToImport, duplicate);
        
        if (action === 'cancel') {
            showToast('Import cancelled', 'info');
            return;
        } else if (action === 'overwrite') {
            // Remove the existing document
            const index = AppState.data.documents.findIndex(doc => doc.id === duplicate.id);
            AppState.data.documents.splice(index, 1);
        } else if (action === 'rename') {
            // Prompt for new name
            const newId = prompt('Enter a new document ID:', documentToImport.id + '-copy');
            if (!newId || !newId.trim()) {
                showToast('Import cancelled - no ID provided', 'info');
                return;
            }
            
            const sanitizedId = newId.trim().toLowerCase()
                .replace(/[^a-z0-9\s-]/g, '')
                .replace(/\s+/g, '-');
            
            // Check if new ID already exists
            if (AppState.data.documents.find(doc => doc.id === sanitizedId)) {
                showToast('A document with this ID already exists', 'error');
                return;
            }
            
            // Update document with new ID
            const oldId = documentToImport.id;
            documentToImport.id = sanitizedId;
            
            // Update all image paths (using project format: images/)
            if (documentToImport.cardImage) {
                const filename = documentToImport.cardImage.split('/').pop();
                documentToImport.cardImage = `images/${sanitizedId}/${filename}`;
            }
            
            documentToImport.sections.forEach(section => {
                if (section.sectionImage && section.sectionImage.trim()) {
                    const filename = section.sectionImage.split('/').pop();
                    section.sectionImage = `images/${sanitizedId}/${filename}`;
                }
            });
            
            // We'll copy images to new folder name
            validation.imageFolder = sanitizedId;
        }
    }
    
    // Fix all image paths to match project structure
    documentToImport = fixDocumentImagePaths(documentToImport);
    
    // Copy images to project
    const sourceImagesPath = bundlePath + '/' + (validation.imagesParentFolder ? validation.imagesParentFolder + '/' : '') + originalId;
    const targetImagesPath = `images/${documentToImport.id}`;
    
    const copyResult = await window.electronAPI.copyDirectoryToProject(
        AppState.currentProjectName,
        sourceImagesPath,
        targetImagesPath
    );
    
    if (!copyResult.success) {
        showToast('Failed to copy images: ' + copyResult.error, 'error');
        return;
    }
    
    // Add document to project
    AppState.data.documents.push(documentToImport);
    
    // Save project
    const saved = await saveProjectData();
    
    if (saved) {
        renderDocumentsList();
        showToast(`Document "${documentToImport.title}" imported successfully!`, 'success');
    }
}

// Validate Document Bundle
async function validateDocumentBundle(bundlePath) {
    // Check for data.json
    const dataJsonPath = bundlePath + '/data.json';
    const dataJsonExists = await window.electronAPI.pathExists(dataJsonPath);
    
    if (!dataJsonExists.exists) {
        return { valid: false, error: 'data.json not found in bundle folder' };
    }
    
    // List directory contents
    const contents = await window.electronAPI.listDirectory(bundlePath);
    
    if (!contents.success) {
        return { valid: false, error: 'Failed to read bundle folder contents' };
    }
    
    // Look for images folder or direct document folder
    let imageFolder = null;
    let imagesParentFolder = null;
    
    // Check if there's an "images" folder
    if (contents.directories.includes('images')) {
        imagesParentFolder = 'images';
        const imagesPath = bundlePath + '/images';
        const imagesContents = await window.electronAPI.listDirectory(imagesPath);
        
        if (imagesContents.success && imagesContents.directories.length > 0) {
            imageFolder = imagesContents.directories[0]; // Take first folder as document ID
        }
    } else {
        // Check for direct document ID folder
        const potentialDocFolders = contents.directories.filter(dir => 
            dir !== 'data' && dir !== 'assets'
        );
        
        if (potentialDocFolders.length > 0) {
            imageFolder = potentialDocFolders[0];
        }
    }
    
    if (!imageFolder) {
        return { valid: false, error: 'No image folder found in bundle' };
    }
    
    return { 
        valid: true, 
        imageFolder,
        imagesParentFolder
    };
}

// Fix Document Image Paths (for project structure, not export)
function fixDocumentImagePaths(doc) {
    // Ensure card image path is correct for PROJECT structure: images/[doc-id]/...
    if (doc.cardImage) {
        const filename = doc.cardImage.split('/').pop();
        // Use project-relative path (images/, not assets/images/)
        doc.cardImage = `images/${doc.id}/${filename}`;
    }
    
    // Fix all section image paths for PROJECT structure
    doc.sections.forEach(section => {
        if (section.sectionImage && section.sectionImage.trim()) {
            const filename = section.sectionImage.split('/').pop();
            // Use project-relative path (images/, not assets/images/)
            section.sectionImage = `images/${doc.id}/${filename}`;
        }
        // If empty string, leave it as is
    });
    
    return doc;
}

// Prompt for Duplicate Action
async function promptDuplicateAction(newDoc, existingDoc) {
    const message = `A document with ${existingDoc.id === newDoc.id ? 'ID' : 'title'} "${existingDoc.id === newDoc.id ? existingDoc.id : existingDoc.title}" already exists.\n\n` +
        `Existing: "${existingDoc.title}" (v${existingDoc.version})\n` +
        `Importing: "${newDoc.title}" (v${newDoc.version})\n\n` +
        `What would you like to do?`;
    
    const action = prompt(message + '\n\nEnter: "overwrite", "rename", or "cancel"', 'cancel');
    
    return action ? action.toLowerCase().trim() : 'cancel';
}

