// Initialize App
document.addEventListener('DOMContentLoaded', async () => {
    initializeEventListeners();
    await initializeApp();
});

// Initialize Application
async function initializeApp() {
    // Check if storage location is configured
    const storageCheck = await window.electronAPI.checkStorageConfigured();
    
    if (!storageCheck.isConfigured) {
        // First launch - ask user to select storage location
        await promptForStorageLocation();
    }
    
    // Now proceed with normal startup
    await checkForExistingProjects();
}

// Prompt user to select storage location
async function promptForStorageLocation() {
    const message = `Welcome to Documentation Manager!\n\nPlease select where you'd like to store your projects.\n\nA folder called "Document Manager/Projects" will be created at your chosen location.`;
    
    if (confirm(message)) {
        const result = await window.electronAPI.setStorageLocation();
        
        if (result.success) {
            showToast(`Projects will be stored at: ${result.path}/Document Manager/Projects`, 'success');
        } else if (result.cancelled) {
            alert('You must select a storage location to use the app. The app will now close.');
            window.close();
        }
    } else {
        alert('You must select a storage location to use the app. The app will now close.');
        window.close();
    }
}

